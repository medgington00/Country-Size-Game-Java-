
public class Country {
	private float size;
	private String name;
	private int rank;
	
	public Country() {	
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setName(String n) {
		this.name = n;
	}
	
	public float getSize() {
		return this.size;
	}
	
	public void setSize(float s) {
		this.size = s;
	}
	
	public float getRank() {
		return this.rank;
	}
	
	public void setRank(int r) {
		this.rank = r;
	}
}
